  private void carregarDadosTabela(){
        String sql = " select empresa, forma, marca,website, produtos from ox_lista_forn order by id";
        
        try{
            
            Connection connectio = ConnectionFactory.getConexao();
            PreparedStatement ps = connectio.prepareStatement(sql);
            
            ResultSet rs = ps.executeQuery();
            
            tabelaLista.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            
            tabelaLista.getColumnModel().getColumn(0).setPreferredWidth(120);
            tabelaLista.getColumnModel().getColumn(1).setPreferredWidth(100);
            tabelaLista.getColumnModel().getColumn(2).setPreferredWidth(150);
            tabelaLista.getColumnModel().getColumn(3).setPreferredWidth(200);
            tabelaLista.getColumnModel().getColumn(4).setPreferredWidth(310);
            tabelaLista.getRowHeight(100);
            
            
            
            model = (DefaultTableModel) tabelaLista.getModel();
            
            while(rs.next()){
               // Integer rsId = rs.getInt("id");
                String rsEmpresa = rs.getString("empresa");
                String rsForma = rs.getString("forma");
                String rsMarca = rs.getString("marca");
                String rsWebSite = rs.getString("website");
                String rsProduto = rs.getString("produtos");
                //preencher os dado na JTable que est√£o retornando do banco de dados
                model.addRow(new Object[]{rsEmpresa, rsForma, rsMarca,  rsWebSite,rsProduto});
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
